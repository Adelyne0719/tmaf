local function safePrep(s)
    if not s then return "" end
    local lowerMap = {
        a="A", b="B", c="C", d="D", e="E", f="F", g="G", h="H", i="I", j="J", k="K", l="L", m="M",
        n="N", o="O", p="P", q="Q", r="R", s="S", t="T", u="U", v="V", w="W", x="X", y="Y", z="Z"
    }
    local r = ""
    for i = 1, #s do
        local c = string.sub(s, i, i)
        if lowerMap[c] then
            r = r .. lowerMap[c]
        elseif c ~= " " then
            r = r .. c
        end
    end
    return r
end

local function safeContains(str, substr)
    if not str or not substr then return false end
    local len1 = #str
    local len2 = #substr
    if len2 == 0 then return true end
    if len1 < len2 then return false end

    for i = 1, len1 - len2 + 1 do
        if string.sub(str, i, i + len2 - 1) == substr then
            return true
        end
    end
    return false
end

function processSearch(player, text, id)
    if text == "" then return end

    local searchKey = safePrep(text)
    local matchedData = nil
    local matchedTitle = ""

    -- 1차 검색: 키워드가 정확히 일치하는지 확인
    for key, data in pairs(glossary) do
        local title = data.title or key
        local cleanTitle = safePrep(title)
        local cleanKey = safePrep(key)

        if cleanKey == searchKey or cleanTitle == searchKey then
            matchedData = data
            matchedTitle = title
            break
        end
    end
    
    -- 2차 검색: 포함하는 단어 찾기 (정확한 일치가 없을 경우)
    if not matchedData then
        for key, data in pairs(glossary) do
            local title = data.title or key
            local cleanTitle = safePrep(title)
            local cleanKey = safePrep(key)

            if safeContains(cleanKey, searchKey) or safeContains(cleanTitle, searchKey) then
                matchedData = data
                matchedTitle = title
                break
            end
        end
    end

    if matchedData then
        local desc = matchedData.desc or ""
        local lineBreaks = select(2, desc:gsub('\n', '\n'))
        local extraLines = math.max(0, lineBreaks - 5) + math.floor(#desc / 35) -- 줄바꿈 및 글자 수 기반 대략적인 줄 수 추정

        local targetBoxHeight = math.max(180, 150 + (extraLines * 25))
        local targetMainHeight = 1000 + (targetBoxHeight - 180)

        self.UI.setAttribute("mainContainer", "height", tostring(targetMainHeight))
        self.UI.setAttribute("descBox", "minHeight", tostring(targetBoxHeight))
        self.UI.setAttribute("descBox", "preferredHeight", tostring(targetBoxHeight))

        self.UI.setAttribute("resultTitle", "text", matchedTitle)
        self.UI.setAttribute("resultDesc", "text", desc)
        return
    end

    self.UI.setAttribute("mainContainer", "height", "1000")
    self.UI.setAttribute("descBox", "minHeight", "180")
    self.UI.setAttribute("descBox", "preferredHeight", "180")
    self.UI.setAttribute("resultTitle", "text", "결과 없음")
    self.UI.setAttribute("resultDesc", "text", "'" .. text .. "'를 찾을 수 없습니다.")
    end

